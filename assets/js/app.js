// Include phoenix_html to handle method=PUT/DELETE in forms and buttons.
import "phoenix_html"
// Establish Phoenix Socket and LiveView configuration.
import {Socket} from "phoenix"
import {LiveSocket} from "phoenix_live_view"

// Theme toggle hook for dark mode with localStorage persistence
const ThemeToggle = {
  mounted() {
    // Initialize theme on mount based on localStorage or system preference
    this.initializeTheme()
    
    // Handle click events on the button
    this.el.addEventListener("click", () => {
      const currentTheme = document.documentElement.getAttribute("data-theme")
      const newTheme = currentTheme === "dark" ? "light" : "dark"
      this.setTheme(newTheme)
    })
    
    this.handleEvent("toggle_theme", ({dark}) => {
      this.setTheme(dark ? "dark" : "light")
    })
  },
  
  initializeTheme() {
    const theme = localStorage.theme === "dark" || 
      (!("theme" in localStorage) && window.matchMedia("(prefers-color-scheme: dark)").matches)
      ? "dark" : "light"
    
    this.setTheme(theme)
  },
  
  setTheme(theme) {
    localStorage.theme = theme
    this.applyTheme(theme === "dark")
    this.updateUI(theme === "dark")
  },
  
  applyTheme(isDark) {
    if (isDark) {
      document.documentElement.setAttribute("data-theme", "dark")
    } else {
      document.documentElement.removeAttribute("data-theme")
    }
  },
  
  updateUI(isDark) {
    const icon = document.getElementById('theme-icon')
    const text = document.getElementById('theme-text')
    
    if (icon && text) {
      if (isDark) {
        icon.textContent = '☀️'
        text.textContent = 'Light'
      } else {
        icon.textContent = '🌙'
        text.textContent = 'Dark'
      }
    }
  }
}

// Import colocated hooks - this will be generated by Phoenix LiveView 1.1+
let hooks = {ThemeToggle}
try {
  const colocatedHooks = await import("phoenix-colocated/pulsar")
  hooks = {...hooks, ...colocatedHooks.hooks}
} catch (e) {
  // Colocated hooks not available yet, that's ok
}

let csrfToken = document.querySelector("meta[name='csrf-token']").getAttribute("content")
let liveSocket = new LiveSocket("/live", Socket, {
  longPollFallbackMs: 2500,
  params: {_csrf_token: csrfToken},
  hooks: hooks
})

// connect if there are any LiveViews on the page
liveSocket.connect()

// expose liveSocket on window for web console debug logs and latency simulation:
// >> liveSocket.enableDebug()
// >> liveSocket.enableLatencySim(1000)  // enabled for duration of browser session
// >> liveSocket.disableLatencySim()
window.liveSocket = liveSocket