defmodule Mix.Tasks.Pulsar.Gen.Button do
  @shortdoc "Generates a Pulsar Button component"
  
  @moduledoc """
  Generates a customizable Button component based on Pulsar's design.

  This task copies the Pulsar Button component into your application,
  allowing you to customize it while maintaining the design system.

      mix pulsar.gen.button

  ## Options

    * `--path` - The path where the component should be generated
      (default: "lib/<app>_web/components/ui")

    * `--module` - The module name for the component
      (default: "<App>Web.Components.UI.Button")

  ## Examples

      # Generate with defaults
      mix pulsar.gen.button

      # Generate to custom path
      mix pulsar.gen.button --path lib/my_app_web/components

      # Generate with custom module name  
      mix pulsar.gen.button --module MyAppWeb.Components.Button

  The generated component will:
  - Be fully customizable (you own the code)
  - Include all Pulsar styling and variants
  - Work with your existing Stellar setup
  - Support all Stellar button features
  """

  use Igniter.Mix.Task

  @impl Igniter.Mix.Task
  def info(_argv, _composing_task) do
    %Igniter.Mix.Task.Info{
      group: :pulsar,
      example: "mix pulsar.gen.button",
      positional: [],
      composes: [],
      schema: [
        path: :string,
        module: :string
      ],
      aliases: [
        p: :path,
        m: :module
      ]
    }
  end

  @impl Igniter.Mix.Task
  def igniter(igniter) do
    argv = igniter.args
    {options, _argv} = OptionParser.parse!(argv, strict: [path: :string, module: :string])
    
    app_name = Igniter.Project.Application.app_name(igniter)
    web_module = Igniter.Libs.Phoenix.web_module(igniter)
    
    # Determine paths and module names
    {component_path, component_module} = get_component_info(app_name, web_module, options)
    
    igniter
    |> ensure_dependencies()
    |> create_button_component(component_path, component_module)
    |> update_components_import(web_module, component_module)
    |> add_success_message(component_module)
  end

  # Get the component file path and module name
  defp get_component_info(app_name, web_module, options) do
    # Default path: lib/<app>_web/components/ui/button.ex  
    default_path = Path.join(["lib", "#{app_name}_web", "components", "ui", "button.ex"])
    default_module = Module.concat([web_module, Components, UI, Button])
    
    component_path = Keyword.get(options, :path, default_path)
    |> ensure_button_filename()
    
    component_module = case Keyword.get(options, :module) do
      nil -> default_module
      module_string -> Module.concat([module_string])
    end
    
    {component_path, component_module}
  end

  # Ensure the path ends with button.ex
  defp ensure_button_filename(path) do
    if String.ends_with?(path, ".ex") do
      path
    else
      Path.join(path, "button.ex")
    end
  end

  # Ensure required dependencies are added
  defp ensure_dependencies(igniter) do
    igniter
    |> Igniter.Project.Deps.add_dep({:stellar, "~> 0.1"})
    |> Igniter.Project.Deps.add_dep({:tailwind_merge, "~> 0.1"})
  end

  # Create the button component file
  defp create_button_component(igniter, component_path, component_module) do
    # Read the source component from Pulsar
    source_path = Path.join([__DIR__, "..", "..", "..", "pulsar", "components", "button.ex"])
    
    case File.read(source_path) do
      {:ok, source_content} ->
        # Transform the source code for the target project
        customized_content = customize_component_code(source_content, component_module)
        
        igniter
        |> Igniter.create_new_file(component_path, customized_content)
        
      {:error, _reason} ->
        # If we can't read the source, generate a minimal version
        minimal_content = generate_minimal_button(component_module)
        
        igniter
        |> Igniter.create_new_file(component_path, minimal_content)
    end
  end

  # Customize the component code for the target project
  defp customize_component_code(source_content, target_module) do
    module_name = Module.split(target_module) |> Enum.join(".")
    
    source_content
    |> String.replace("Pulsar.Components.Button", module_name)
    |> String.replace(
      "This function wraps Stellar.Components.Button with Pulsar's styling system.",
      "This function wraps Stellar.Components.Button with custom styling.\n\n  Generated by `mix pulsar.gen.button` - you can customize this component."
    )
    |> add_customization_comment()
  end

  # Add comment about customization at the top of the file
  defp add_customization_comment(content) do
    """
    # Generated by Pulsar - feel free to customize!
    # This component was generated from Pulsar.Components.Button
    # You can modify styling, add new variants, or change behavior as needed.

    """ <> content
  end

  # Generate a minimal button if we can't read the source
  defp generate_minimal_button(component_module) do
    module_name = Module.split(component_module) |> Enum.join(".")
    
    """
    # Generated by Pulsar - feel free to customize!

    defmodule #{module_name} do
      use Phoenix.Component
      import Stellar.Components.Button, only: [button: 1]

      attr :variant, :string, 
        default: "primary",
        values: ~w(primary secondary ghost outline)
        
      attr :size, :string, 
        default: "md",
        values: ~w(sm md lg)
        
      attr :class, :string, default: ""
      attr :rest, :global
      slot :inner_block, required: true

      def button(assigns) do
        assigns = assign(assigns, :class,
          TailwindMerge.merge([
            base_classes(),
            variant_classes(assigns.variant),
            size_classes(assigns.size),
            assigns.class
          ])
        )

        ~H\"\"\"
        <Stellar.Components.Button.button {...assigns}>
          <%= render_slot(@inner_block) %>
        </Stellar.Components.Button.button>
        \"\"\"
      end

      defp base_classes do
        "inline-flex items-center justify-center font-medium rounded-lg transition-colors"
      end

      defp variant_classes("primary"), do: "bg-blue-500 text-white hover:bg-blue-600"
      defp variant_classes("secondary"), do: "bg-gray-200 text-gray-900 hover:bg-gray-300"
      defp variant_classes("ghost"), do: "hover:bg-gray-100"
      defp variant_classes("outline"), do: "border border-gray-300 hover:bg-gray-50"

      defp size_classes("sm"), do: "h-8 px-3 text-sm"
      defp size_classes("md"), do: "h-10 px-4"
      defp size_classes("lg"), do: "h-12 px-6 text-lg"
    end
    """
  end

  # Update the main components file to import the new component
  defp update_components_import(igniter, web_module, component_module) do
    components_path = Path.join(["lib", to_string(web_module) |> Macro.underscore(), "components.ex"])
    
    # Try to find and update existing components file
    case Igniter.exists?(igniter, components_path) do
      true ->
        igniter
        |> Igniter.update_elixir_file(components_path, fn zipper ->
          # Add import for the new component
          import_line = "import #{inspect(component_module)}"
          Sourceror.Zipper.append_child(zipper, import_line)
        end)
        
      false ->
        # Create a basic components file if it doesn't exist
        components_content = """
        defmodule #{web_module}.Components do
          @moduledoc \"\"\"
          Shared components for #{web_module}.
          \"\"\"
          
          import #{inspect(component_module)}
        end
        """
        
        igniter
        |> Igniter.create_new_file(components_path, components_content)
    end
  end

  # Add success message
  defp add_success_message(igniter, component_module) do
    message = """
    
    ✨ Button component generated successfully!
    
    Component: #{inspect(component_module)}
    
    Usage:
      import #{inspect(component_module)}
      
      # In your templates
      <.button variant="primary">Click me</.button>
      <.button variant="outline" size="lg">Large button</.button>
    
    Next steps:
    1. Import the component in your LiveView/component modules
    2. Customize the styling and variants as needed
    3. Add any additional props or slots you need
    
    The component is now fully yours to customize!
    """
    
    Igniter.add_notice(igniter, message)
  end
end